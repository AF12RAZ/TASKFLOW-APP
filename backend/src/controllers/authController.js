/* ADMIN FORCE RESET PASSWORD (FOR TROUBLESHOOTING) */
export const adminForceResetPassword = async (req, res) => {
  try {
    const { email, newPassword } = req.body;

    if (!email || !newPassword) {
      return res.status(400).json({ message: "Email and new password required" });
    }

    if (newPassword.length < 6) {
      return res.status(400).json({ message: "Password must be at least 6 characters" });
    }

    const user = await User.findOne({ email });
    if (!user) {
      return res.status(404).json({ message: "User not found" });
    }

    const hashedPassword = await bcrypt.hash(newPassword, 10);
    user.password = hashedPassword;
    await user.save();

    console.log(`Password reset for user: ${email}`);

    res.json({ 
      success: true,
      message: "Password reset successfully. You can now login with the new password." 
    });
  } catch (error) {
    console.error("Force reset password error:", error);
    res.status(500).json({ message: "Error resetting password" });
  }
};